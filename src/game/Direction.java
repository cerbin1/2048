package game;

enum Direction {
    Down(0, -1), Up(0, 1), Left(-1, 0), Right(1, 0);

    private final int x;
    private final int y;

    Direction(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
